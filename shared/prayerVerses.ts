/** 날짜('YYYY-MM-DD')별로 고정된 묵상 구절 선택 — 같은 날은 항상 같은 구절 */
export function pickDailyVerse(date: string): CuratedVerse {
  let h = 0;
  for (let i = 0; i < date.length; i++) {
    h = (h * 31 + date.charCodeAt(i)) >>> 0;
  }
  return PRAYER_VERSES[h % PRAYER_VERSES.length];
}
